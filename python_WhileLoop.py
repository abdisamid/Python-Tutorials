# basic while loop syntax in python => :
x = 0
while x < 5:
  x += 1
  print(x) # this outputs => : 1 2 3 4 5 
  

count = 0
while (count < 9):
   print ('The count is:', count)
   count = count + 1

print ("Good bye!")
